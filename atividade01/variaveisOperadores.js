let a = 10;
let b = 15;

let soma = a + b; 
console.log(`Resultado de {${a} + {${b}} = ${soma}}`);

let sub = a - b; 
console.log(`Resultado de {${a} - {${b}} = ${sub}}`);

let mult = a * b; 
console.log(`Resultado de {${a} * {${b}} = ${mult}}`);

let div = a / b; 
console.log(`Resultado de {${a} / {${b}} = ${div}}`);